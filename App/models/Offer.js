class Offer {
    constructor(
      id,
      categoryIds,
      companyName,
      image,
      simpleText,
      details,
      aboutCompany,
    ) {
      this.id = id;
      this.categoryIds = categoryIds;
      this.companyName = companyName;
      this.image = image;
      this.simpleText = simpleText;
      this.details = details;
      this.aboutCompany = aboutCompany;
    }
  }
  
  export default Offer;